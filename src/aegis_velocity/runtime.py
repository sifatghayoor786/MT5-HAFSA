def _sim_walk(
    sim: SimMt5Client,
    symbols: list[str],
    steps: int,
    seed: int = 42,
    spreads: SpreadHistory | None = None,
    on_tick: TickCallback | None = None,
) -> None:
    """Deterministic seeded random walk. SIMULATION DATA, labelled as such.
    `on_tick` receives EVERY scripted tick synchronously (no pump sampling)."""
    rng = random.Random(seed)
    prices = {s: (155.0 if s.endswith("JPY") else 2400.0 if s == "XAUUSD" else 1.10)
              for s in symbols}
    for _ in range(steps):
        for symbol in symbols:
            spec = sim.specs[symbol]
            point = spec.point
            drift = rng.gauss(0, 6) * point
            prices[symbol] = max(prices[symbol] + drift, 100 * point)
            spread_pts = max(4, min(14, round(rng.gauss(8, 2))))
            bid = round(prices[symbol], spec.digits)
            ask = round(bid + spread_pts * point, spec.digits)
            tick = sim.push_tick(symbol, bid, ask, advance_s=1.0)
            if spreads is not None:
                spreads.record(symbol, tick.time, spread_pts)
            if on_tick is not None:
                on_tick(symbol, tick)


def _sim_bars(sim: SimMt5Client, symbol: str, tf_s: int, count: int) -> None:
    spec = sim.specs[symbol]
    base = 155.0 if symbol.endswith("JPY") else 2400.0 if symbol == "XAUUSD" else 1.10
    rng = random.Random(hash(symbol) & 0xFFFF)
    bars = []
    t0 = sim.server_time - timedelta(seconds=tf_s * (count + 1))
    price = base
    for i in range(count):
        o = price
        h = o + abs(rng.gauss(0, 20)) * spec.point
        low = o - abs(rng.gauss(0, 20)) * spec.point
        c = rng.uniform(low, h)
        bars.append(Bar(time=t0 + timedelta(seconds=tf_s * i), open=o, high=h, low=low,
                        close=c))
        price = c
    sim.set_bars(symbol, tf_s, bars)


