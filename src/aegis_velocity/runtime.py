def positions_cli(root: Path, which: str) -> int:
    desk = build_desk(root, TradingMode.SHADOW)
    try:
        if which == "positions":
            for p in desk.gateway.call(P2_ACCOUNT, "positions_get"):
                print(f"  #{p.ticket} {p.symbol} {p.side.value} {p.volume} "
                      f"@{p.price_open} sl={p.sl} tp={p.tp} pnl={p.profit:.2f}")
            print("(no positions)" if not desk.gateway.call(P2_ACCOUNT, "positions_get")
                  else "")
        elif which == "pendings":
            orders = desk.gateway.call(P2_ACCOUNT, "orders_get")
            for o in orders:
                print(f"  #{o.ticket} {o.symbol} {o.kind.value} {o.volume} @{o.price}")
            if not orders:
                print("(no pending orders)")
        elif which == "reconcile":
            from aegis_velocity.execution.reconcile import startup_reconcile

            report = startup_reconcile(
                desk.gateway, desk.ledger, desk.intents,
                now_fn=lambda: datetime.now(UTC),
            )
            print(f"reconcile: {report}")
        elif which == "ea-status":
            print("EA bridge not running in this CLI invocation; "
                  "ea-status is meaningful inside `run` on the trading host.")
        return 0
    finally:
        desk.stop()


def activate_live_cli(root: Path) -> int:
    """HUMAN-ONLY (TTY enforced by cli.py). Full §13 arming ceremony."""
    cfg = load_desk_config(root)
    available, detail = real_client_available()
    if not available:
        print(f"activate-live requires the real MT5 terminal: {detail}")
        print("USER-ACTION: run this on the Windows trading host.")
        return 1
    if not cfg.env.live_trading_enabled:  # pragma: no cover - real host only
        print("LIVE_TRADING_ENABLED is not true in .env — refusing.")
        return 1
    # pragma: no cover start (interactive, real host only)
    desk = build_desk(root, TradingMode.LIVE_CANARY)
    try:
        info = desk.gateway.call(P2_ACCOUNT, "account_info")
        if info is None:
            print("no account info; aborting")
            return 1
        if info.login not in (cfg.env.live_account_allowlist or ()):
            print("account not in LIVE_ACCOUNT_ALLOWLIST — refusing")
            return 1
        if info.margin_mode != "HEDGING" or not info.trade_allowed or info.is_investor:
            print("account fails hedging/permission checks — refusing")
            return 1
        print(f"account {mask_login(info.login)} @ {info.server} "
              f"balance={info.balance:.2f} equity={info.equity:.2f}")
        print(f"limits: {cfg.risk.model_dump()}")
        verify = desk.ledger.verify()
        print(f"self-tests: ledger={'OK' if verify.ok else 'FAIL'}")
        if not verify.ok:
            return 1
        typed = getpass.getpass(f"type the phrase to arm ({ARMING_PHRASE!r}): ")
        if typed != ARMING_PHRASE:
            print("phrase mismatch — NOT armed")
            return 1
        token = write_token(root / "data" / "arming.json", info.login, info.server,
                            "LIVE_CANARY", cfg.config_hash())
        desk.ledger.append("arming", {"account": mask_login(info.login),
                                      "server": info.server, "mode": token.mode,
                                      "config_hash": token.config_hash})
        print("ARMED (LIVE_CANARY): first fills are min-lot canary. Red banner active.")
        return 0
    finally:
        desk.stop()
    # pragma: no cover end


def promote_live_full_cli(root: Path) -> int:
    """HUMAN-ONLY. Exits canary only after the §11 canary gate passes."""
    cfg = load_desk_config(root)
    data = root / "data"
    counters = PersistedCounters(PersistedState(data / "counters.json"))
    fills = counters.get_int("canary_fills")
    if fills < cfg.risk.canary_fills:
        print(f"canary gate NOT passed: {fills}/{cfg.risk.canary_fills} min-lot fills")
        return 1
    status = validate_token(
        data / "arming.json", cfg.env.login or 0, cfg.env.server, cfg.config_hash()
    )
    if not status.armed:
        print(f"not armed: {status.reason}")
        return 1
    write_token(data / "arming.json", cfg.env.login or 0, cfg.env.server, "LIVE",
                cfg.config_hash())
    print("promoted to LIVE full sizing (arming token updated).")
    return 0


