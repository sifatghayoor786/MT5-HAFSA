TickCallback = Callable[[str, Tick], None]


@dataclass
class Desk:
    root: Path
    cfg: DeskConfig
    mode: TradingMode
    client: Mt5Client
    gateway: Mt5Gateway
    ledger: Ledger
    intents: IntentStore
    clock: BrokerClock
    cost_engine: CostEngine
    council: Council
    risk: RiskCommander
    auditor: Auditor
    recorder: TickRecorder
    is_sim: bool

    def stop(self) -> None:
        self.auditor.stop()
        self.gateway.stop()
        self.ledger.close()


def make_sim_client(cfg: DeskConfig) -> SimMt5Client:
    specs = {name: default_spec(name) for name in cfg.symbols.universe}
    return SimMt5Client(specs=specs, balance=10_000.0)


def build_desk(root: Path, mode: TradingMode) -> Desk:
    cfg = load_desk_config(root)
    data = root / "data"
    data.mkdir(exist_ok=True)

    available, _detail = real_client_available()
    if available:  # pragma: no cover - Windows-only path
        from aegis_velocity.mt5.real import RealMt5Client

        client: Mt5Client = RealMt5Client()
        is_sim = False
        login = cfg.env.login or 0
        password = cfg.env.password.get_secret_value() if cfg.env.password else ""
        server = cfg.env.server
    else:
        sim = make_sim_client(cfg)
        client = sim
        is_sim = True
        login, password, server = sim.login, "sim", sim.server

    if not client.initialize(cfg.env.terminal_path, login, password, server):
        raise RuntimeError(f"MT5 initialize failed: {client.last_error()}")

    gateway = Mt5Gateway(client)
    gateway.start()
    ledger = Ledger(data / "aegis_velocity.db", journal_path=data / "journal.jsonl")
    intents = IntentStore(ledger.connection, ledger.db_lock)
    clock = BrokerClock(persist_path=data / "clock.json")
    calendar = NewsCalendar(data / "calendar.json", cfg.sessions.news_blackout)
    spreads = SpreadHistory(persist_path=data / "spreads.json",
                            history_days=cfg.costs.liquidity_window.history_days)
    cost_engine = CostEngine(cfg.costs, cfg.risk, cfg.sessions, calendar, spreads)
    council = Council(cost_engine=cost_engine, risk_cfg=cfg.risk, clock=clock)
    anchors = EquityAnchors(PersistedState(data / "anchors.json"))
    counters = PersistedCounters(PersistedState(data / "counters.json"))
    risk = RiskCommander(cfg.risk, cfg.correlations, anchors, counters, mode)
    auditor = Auditor(ledger)
    auditor.start()
    recorder = TickRecorder(data / "ticks")
    return Desk(
        root=root, cfg=cfg, mode=mode, client=client, gateway=gateway, ledger=ledger,
        intents=intents, clock=clock, cost_engine=cost_engine, council=council,
        risk=risk, auditor=auditor, recorder=recorder, is_sim=is_sim,
    )


