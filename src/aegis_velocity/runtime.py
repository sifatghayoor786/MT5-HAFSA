def connect_and_report(root: Path) -> int:  # pragma: no cover - real terminal only
    cfg = load_desk_config(root)
    from aegis_velocity.mt5.real import RealMt5Client

    client = RealMt5Client()
    password = cfg.env.password.get_secret_value() if cfg.env.password else ""
    if not client.initialize(cfg.env.terminal_path, cfg.env.login or 0, password,
                             cfg.env.server):
        print(f"initialize failed: {client.last_error()}")
        return 1
    info = client.account_info()
    if info is None:
        print("no account info")
        return 1
    print(
        f"connected: login={mask_login(info.login)} server={info.server} "
        f"currency={info.currency} margin_mode={info.margin_mode} "
        f"trade_mode={info.trade_mode} trade_allowed={info.trade_allowed}"
    )
    if info.margin_mode != "HEDGING":
        print("REFUSED for execution: netting account (SHADOW analysis only).")
    client.shutdown()
    return 0


def discover_symbols_cli(root: Path) -> int:
    desk = build_desk(root, TradingMode.SHADOW)
    try:
        names = desk.gateway.call(P3_DATA, "symbols_get_names")
        result = discover_symbols(names, desk.cfg.symbols.universe)
        persist_symbol_map(root / "data" / "symbol_map.json", result.mapping)
        print(f"mapped {len(result.mapping)}/{len(desk.cfg.symbols.universe)} symbols"
              + (" [SIM]" if desk.is_sim else ""))
        for canonical, broker in sorted(result.mapping.items()):
            print(f"  {canonical} -> {broker}")
        for canonical, options in result.ambiguous.items():
            print(f"  AMBIGUOUS {canonical}: {options} (confirm manually)")
        for missing in result.missing:
            print(f"  MISSING {missing}")
        desk.ledger.append("symbol_discovery", {
            "mapping": result.mapping, "ambiguous": result.ambiguous,
            "missing": result.missing, "sim": desk.is_sim,
        })
        return 0
    finally:
        desk.stop()


def scalp_eligibility_cli(root: Path) -> int:
    desk = build_desk(root, TradingMode.SHADOW)
    try:
        window_minutes = 5.0
        if desk.is_sim:
            sim = desk.client
            assert isinstance(sim, SimMt5Client)
            _sim_walk(sim, desk.cfg.symbols.universe, steps=int(window_minutes * 60))
        verdicts = []
        for symbol in desk.cfg.symbols.universe:
            spec = desk.gateway.call(P3_DATA, "symbol_info", symbol)
            if spec is None:
                continue
            since = datetime.now(UTC) - timedelta(days=2)
            ticks = desk.gateway.call(P3_DATA, "copy_ticks_from", symbol, since, 100_000)
            samples = [t.spread_points(spec.point) * 1.0 for t in ticks]
            verdict = measure_scalp_eligibility(
                spec, samples, window_minutes, desk.cfg.symbols.scalp_eligibility
            )
            verdicts.append(verdict)
            state = "ELIGIBLE" if verdict.eligible else "DISQUALIFIED"
            print(f"  {symbol}: {state} spread_p50={verdict.spread_p50_points:.1f} "
                  f"ticks/min={verdict.ticks_per_minute:.1f} "
                  f"stops={verdict.stops_level_points} {verdict.reasons}")
        persist_eligibility(root / "data" / "scalp_eligibility.json", verdicts)
        print(("[SIM] " if desk.is_sim else "") + "verdicts written to "
              "data/scalp_eligibility.json")
        return 0
    finally:
        desk.stop()


def record_ticks_cli(root: Path, hours: float) -> int:
    desk = build_desk(root, TradingMode.SHADOW)
    try:
        if desk.is_sim:
            sim = desk.client
            assert isinstance(sim, SimMt5Client)
            steps = int(hours * 3600)
            print(f"[SIM] recording {steps} scripted ticks per symbol "
                  "(simulation data, not broker data)")
            desk.gateway.subscribe_ticks(desk.recorder.on_tick)
            desk.gateway._pump_symbols = list(desk.cfg.symbols.universe)
            for s in desk.cfg.symbols.universe:
                desk.gateway.call(P3_DATA, "symbol_select", s, True)
            import threading

            pump = threading.Thread(target=desk.gateway._run_pump, daemon=True)
            pump.start()
            _sim_walk(sim, desk.cfg.symbols.universe, steps=min(steps, 600))
            time.sleep(0.5)
        else:  # pragma: no cover - real terminal only
            for s in desk.cfg.symbols.universe:
                desk.gateway.call(P3_DATA, "symbol_select", s, True)
            desk.gateway.subscribe_ticks(desk.recorder.on_tick)
            deadline = time.monotonic() + hours * 3600
            while time.monotonic() < deadline:
                time.sleep(1.0)
        desk.recorder.close()
        print(f"recorded tick counts: {desk.recorder.counts}")
        return 0
    finally:
        desk.stop()


def run_desk_cli(root: Path, mode: str, duration_s: float) -> int:
    trading_mode = TradingMode(mode.upper())
    desk = build_desk(root, trading_mode)
    try:
        return _scan_loop(desk, duration_s)
    finally:
        desk.stop()


def _scan_loop(desk: Desk, duration_s: float) -> int:
    """Shadow/demo scan: ticks -> strategies -> council -> ledger. In SHADOW no
    order is ever sent (preflight hard-blocks); this loop only records truth."""
    symbols = desk.cfg.symbols.universe
    for s in symbols:
        desk.gateway.call(P3_DATA, "symbol_select", s, True)
    windows: dict[str, list[Tick]] = {s: [] for s in symbols}
    cooldown_until: dict[tuple[str, str], datetime] = {}
    decisions = 0
    signals_seen = 0

    def on_tick(symbol: str, tick: Tick) -> None:
        nonlocal decisions, signals_seen
        desk.clock.observe_tick(tick.time)
        desk.recorder.on_tick(symbol, tick)
        spec = desk.client.symbol_info(symbol)
        if spec is None:
            return
        window = windows[symbol]
        window.append(tick)
        if len(window) > 200:
            del window[: len(window) - 200]
        if len(window) < 70:
            return
        bars = tuple(desk.client.copy_rates(symbol, 300, 48))
        for sid, strategy_cfg in desk.cfg.strategies.strategies.items():
            if not strategy_cfg.enabled:
                continue
            until = cooldown_until.get((sid, symbol))
            if until is not None and tick.time < until:
                continue  # per-symbol strategy cooldown (§7 declaration)
            ctx = StrategyContext(
                strategy_id=sid, symbol=symbol, spec=spec, ticks=tuple(window),
                bars=bars, server_now=tick.time, cfg=strategy_cfg,
            )
            for signal in STRATEGY_REGISTRY[sid](ctx):
                signals_seen += 1
                cooldown_until[(sid, symbol)] = tick.time + timedelta(
                    seconds=strategy_cfg.cooldown_s
                )
                desk.ledger.append(
                    "signal", signal.model_dump(mode="json"),
                    correlation_id=signal.correlation_id,
                )
                result = desk.council.decide(
                    CouncilInputs(
                        signal=signal, spec=spec, tick=tick, server_now=tick.time,
                        data_integrity=95.0, conflicting_position=False,
                        in_flight_intent=False, strategy_quarantined=False,
                    )
                )
                decisions += 1
                desk.ledger.append(
                    "decision", result.decision.model_dump(mode="json"),
                    correlation_id=signal.correlation_id,
                )

    if desk.is_sim:
        sim = desk.client
        assert isinstance(sim, SimMt5Client)
        for s in symbols:
            _sim_bars(sim, s, 300, 60)
        steps = int(duration_s) if duration_s > 0 else 300
        print(f"[SIM] {desk.mode.value} scan over {steps} scripted ticks/symbol "
              "(simulation data)")
        # every scripted tick is delivered synchronously: deterministic scan
        _sim_walk(sim, list(symbols), steps=steps, spreads=desk.cost_engine.spreads,
                  on_tick=on_tick)
    else:  # pragma: no cover - real terminal only
        desk.gateway.subscribe_ticks(on_tick)
        desk.gateway._pump_symbols = list(symbols)
        import threading

        pump = threading.Thread(target=desk.gateway._run_pump, daemon=True)
        pump.start()
        deadline = time.monotonic() + (duration_s or 3600.0)
        while time.monotonic() < deadline:
            time.sleep(1.0)

    desk.ledger.append(
        "scan_summary",
        {"mode": desk.mode.value, "signals": signals_seen, "decisions": decisions,
         "sim": desk.is_sim},
    )
    verify = desk.ledger.verify()
    print(
        f"scan done: {signals_seen} signals, {decisions} decisions recorded; "
        f"ledger {'OK' if verify.ok else 'BROKEN'} ({verify.rows} rows)"
    )
    return 0


def emergency_stop_cli(root: Path, flatten: bool) -> int:
    """Emergency stop: halt entries, invalidate arming, optionally flatten."""
    from aegis_velocity.arming import disarm

    data = root / "data"
    disarm(data / "arming.json", reason="emergency-stop")
    ledger = Ledger(data / "aegis_velocity.db")
    ledger.append("halt", {"reason": "EMERGENCY_STOP", "flatten": flatten,
                           "source": "cli"})
    ledger.close()
    print("EMERGENCY STOP recorded: arming invalidated, entries halted.")
    if flatten:
        print("flatten requested: on a live host the desk closes all AEG positions"
              " and cancels pendings via gateway P0 + EA flatten_all command.")
    return 0


def report_cli(root: Path, which: str) -> int:
    ledger = Ledger(root / "data" / "aegis_velocity.db")
    try:
        if which == "parity-check":
            print("run: python3 -m pytest tests/test_parity.py -q")
            return 0
        kinds = {
            "report": None,
            "cost-report": "decision",
            "latency-report": "execution",
            "spread-report": "signal",
        }
        rows = list(ledger.rows(kind=kinds.get(which), limit=20))
        print(f"{which}: last {len(rows)} ledger rows"
              f" (chain: {'OK' if ledger.verify().ok else 'BROKEN'})")
        for row in rows[:10]:
            print(f"  #{row.seq} {row.ts_utc} {row.kind} corr={row.correlation_id}")
        return 0
    finally:
        ledger.close()


def backtest_cli(root: Path, strategy: str, symbol: str) -> int:
    from aegis_velocity.audit.stats import expectancy, profit_factor
    from aegis_velocity.backtest.tick_engine import StressConfig, run_strategy_over_ticks

    cfg = load_desk_config(root)
    if strategy not in cfg.strategies.strategies:
        print(f"unknown strategy {strategy}")
        return 1
    ticks = load_recorded_ticks(root / "data" / "ticks", symbol)
    if len(ticks) < 500:
        print(f"INSUFFICIENT_DATA: {len(ticks)} recorded ticks for {symbol} "
              "(need >= 500). Record ticks first (record-ticks). Bar backtests are "
              "NON-EVIDENCE and are not offered.")
        return 1
    spec = default_spec(symbol)
    strategy_cfg = cfg.strategies.strategies[strategy]

    def make_signals(window: tuple[Tick, ...]) -> list[Any]:
        ctx = StrategyContext(
            strategy_id=strategy, symbol=symbol, spec=spec, ticks=window, bars=(),
            server_now=window[-1].time, cfg=strategy_cfg,
        )
        return STRATEGY_REGISTRY[strategy](ctx)

    for label, stress in (("base", None), ("stressed", StressConfig.stressed())):
        result = run_strategy_over_ticks(
            strategy, spec, ticks, make_signals, stress=stress,
        )
        rs = result.returns_r
        print(
            f"[{label}] {strategy}/{symbol}: fills={len(rs)} "
            f"signals={result.signals_seen} untriggered={result.signals_untriggered} "
            f"expectancy={expectancy(rs):.4f}R PF={profit_factor(rs):.3f}"
            if rs else f"[{label}] no fills — INSUFFICIENT_DATA for any verdict"
        )
    print("NOTE: tick source is "
          + ("SIM-RECORDED (non-evidence for live promotion)" if True else "live"))
    return 0


